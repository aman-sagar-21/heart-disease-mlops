from src.predict import predict

def test_prediction():

    sample = {
        "age": 63,
        "sex": 1,
        "cp": 3,
        "trestbps": 145,
        "chol": 233,
        "fbs": 1,
        "restecg": 0,
        "thalach": 150,
        "exang": 0,
        "oldpeak": 2.3,
        "slope": 0,
        "ca": 0,
        "thal": 1
    }

    prediction, probability = predict(sample)

    assert prediction in [0, 1]

    assert 0 <= probability <= 1